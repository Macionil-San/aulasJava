package exemplos;

public class Exercicio03 {
    public static void main(String[] args) {
        int num01 = 3, num02 = 7;

        boolean resposta;

        resposta = num01>num02;
        System.out.println(num01 + " > " + num02 + resposta);
        resposta = num01<num02;
        System.out.println(num01 + " < " + num02 + resposta);
        resposta = num01>=num02;
        System.out.println(num01 + " >= " + num02 + resposta);
        resposta = num01<=num02;
        System.out.println(num01 + " <= " + num02 + resposta);

        resposta = num01 != num02;
        System.out.println(num01 + " != " + num02 + resposta);



    }
}
